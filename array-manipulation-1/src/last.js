/* exported last */
/*
  - define a function named last with one parameter "array"
  - get the total number of index in the array using the length method
  - subtract the total index by 1
  - the array gets the last index number from the result of the expression of the
  length method of the array object being subtracted by 1
  - return output
*/

function last(array) {
  return array[array.length - 1];
}
