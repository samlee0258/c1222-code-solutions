/*
  - define a function named isLowerCased with one parameter "word"
  - write a conditional statement
    - if the word is strictly equal to all lowercased letters, return true
    - else return false
*/

function isLowerCased(word) {
  if (word === word.toLowerCase(word)) {
    return true;
  } else {
    return false;
  }
}
